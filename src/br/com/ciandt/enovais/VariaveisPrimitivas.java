package br.com.ciandt.enovais;

public class VariaveisPrimitivas {

	public static void main(String[] args) {
	byte jByte = 1;
	short jShort = 10;
	int jInt = 100;
	long jLong = 1000;
	float jFloat = 100.1234567890f;
	double jDouble = 100.1234567890123456789f;
	char jCharN = 97;
	char jCharC = 'a';
	boolean jBoolean = true;
	
	System.out.println(jByte);
	System.out.println(jShort);
	System.out.println(jInt);
	System.out.println(jLong);
	System.out.println(jFloat);
	System.out.println(jDouble);
	System.out.println(jCharN);
	System.out.println(jCharC);
	System.out.println(jBoolean);
	
	}
}
