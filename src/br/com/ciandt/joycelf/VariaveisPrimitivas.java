package br.com.ciandt.joycelf;

public class VariaveisPrimitivas {
	

	public static void main(String[] args) {	
		byte byteVariable = 1;
		short shortVariable = 10; 
		int intVariable =100; 
		long longVariable = 1000; 
		float floatVariable = 100.1234567890f; 
		double doubleVariable = 100.1234567890123456789d; 
		char charVariableA = 97; 
		char charVariableB = 'a';
		boolean booleanVariable = true;
		
		System.out.println(byteVariable);
		System.out.println(shortVariable);
		System.out.println(intVariable);
		System.out.println(longVariable);
		System.out.println(floatVariable);
		System.out.println(doubleVariable);
		System.out.println(charVariableA);
		System.out.println(charVariableB);
		System.out.println(booleanVariable);
	}
}
