package br.com.ciandt.csartini;

public class VariaveisPrimitivas {

	
	public static void main(String[] args) {
		
		byte numByte =1;
		short caracShort =10; 
		int numInt =100;
		long caracLong =1000;
		float numFloat =100.1234567890f; 
		double numDouble =100.1234567890123456789d;
		char caracChar =97;
		char caracCharA = 'a'; 
		boolean numBool =true;

	System.out.println(numByte);
	System.out.println(caracShort);
	System.out.println(numInt);
	System.out.println(caracLong);
	System.out.println(numFloat);
	System.out.println(numDouble);
	System.out.println(caracChar);
	System.out.println(caracCharA);
	System.out.println(numBool);
	
	}
}
