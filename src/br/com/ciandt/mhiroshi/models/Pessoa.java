package br.com.ciandt.login.models;

public class Pessoa {

	public String nome;
	public String idade;

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getIdade() {
		return idade;
	}

	public void setIdade(String string) {
		this.idade = string;
	}
	
}
