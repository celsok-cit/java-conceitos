package br.com.ciandt.login;

public class VariaveisPrimitivas {

	public static void main(String[] args) {

		byte firstEx=(byte)1;
		short secondEx=(short)10;
		int thirdEx=100;
		long forthEx=1000l;
		float fifthEx=100.1234567890f;	
		double sixthEx=100.1234567890123456789d;
		char seventhEx=97;
		char eighthEx='a';
		boolean ninethEx=true; 	
		
		System.out.println(firstEx + "byte: " + secondEx + "short: " + thirdEx + "int: " + forthEx + "long: " + fifthEx + "float: " + sixthEx + "double: " + seventhEx + "char: " + eighthEx + "char: " + ninethEx + "boolean: ");
	}
}

